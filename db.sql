DROP DATABASE baemin1;
create database baemin1;
use baemin1;

CREATE TABLE `User` (
	`userId`	int(20)	NOT NULL,
	`username`	varchar(100)	NOT NULL,
	`phone`	varchar(20)	NOT NULL,
	`email`	varchar(100)	NOT NULL,
	`grade`	varchar(100)	NOT NULL	DEFAULT '고마운분',
	`createdDate`	timestamp	NOT NULL,
	`modifiedDate`	timestamp	NULL,
	`status`	varchar(255)	NOT NULL	DEFAULT '일반'
);

CREATE TABLE `Coupons` (
	`couponId`	int(20)	NOT NULL,
	`storeId`	int(20)	NOT NULL,
	`name`	varchar(255)	NOT NULL,
	`content`	varchar(255)	NULL,
	`deductedPrice`	int(11)	NOT NULL,
	`minDeliveryPrice`	int(11)	NOT NULL	DEFAULT 0,
	`createdDate`	timestamp	NOT NULL,
	`modifiedDate`	timestamp	NULL,
	`status`	varchar(255)	NOT NULL	DEFAULT '일반'
);

CREATE TABLE `Orders` (
	`orderId`	int(20)	NOT NULL,
	`storeId`	int(20)	NOT NULL,
	`userId`	int(20)	NOT NULL,
	`paymentMethod`	varchar(255)	NOT NULL,
	`totalPrice`	int(11)	NOT NULL,
	`requests`	varchar(255)	NULL,
	`createdDate`	timestamp	NOT NULL,
	`modifiedDate`	timestamp	NULL,
	`status`	varchar(255)	NOT NULL	DEFAULT '일반'
);

CREATE TABLE `Reviews` (
	`reviewId`	int(20)	NOT NULL,
	`storeId`	int(20)	NOT NULL,
	`username`	varchar(100)	NOT NULL,
	`userId`	int(20)	NOT NULL,
	`menuId`	int(20)	NOT NULL,
	`rating`	int(1)	NOT NULL,
	`createdDate`	timestamp	NOT NULL,
	`modifiedDate`	timestamp	NULL,
	`status`	varchar(255)	NOT NULL	DEFAULT '일반'
);

CREATE TABLE `Stores` (
	`storeId`	int(20)	NOT NULL,
	`name`	varchar(255)	NOT NULL,
	`category`	varchar(20)	NOT NULL,
	`latitude`	decimal(13,10)	NOT NULL,
	`longitude`	decimal(13,10)	NOT NULL,
	`deliveryRadius`	decimal(2,1)	NOT NULL,
	`address`	varchar(255)	NOT NULL,
	`storePictureUrl`	text	NULL,
	`phone`	varchar(20)	NOT NULL,
	`content`	varchar(255)	NULL,
	`minDeliveryPrice`	int(11)	NOT NULL,
	`mindeliveryTip`	int(11)	NOT NULL	DEFAULT 0,
	`maxdeliveryTip`	int(11)	NOT NULL	DEFAULT 0,
	`minDeliveryTime`	int(3)	NULL,
	`maxDeliveryTime`	int(3)	NULL,
	`rating`	decimal(2,1)	NOT NULL	DEFAULT 0,
	`reviewCount`	int(11)	NOT NULL	DEFAULT 0,
	`startTime`	time	NOT NULL,
	`endTime`	time	NOT NULL,
	`closedDays`	int(7)	NULL,
	`createdDate`	timestamp	NOT NULL,
	`modifiedDate`	timestamp	NULL,
	`status`	varchar(255)	NOT NULL	DEFAULT '일반'
);

CREATE TABLE `Menu` (
	`menuId`	int(20)	NOT NULL,
	`storeId`	int(20)	NOT NULL,
	`category`	varchar(100)	NOT NULL,
	`name`	varchar(255)	NOT NULL,
	`price`	int(11)	NOT NULL,
	`menuPictureUrl`	text	NULL,
	`createdDate`	timestamp	NOT NULL,
	`modifiedDate`	timestamp	NULL,
	`status`	varchar(255)	NOT NULL	DEFAULT '일반'
);

CREATE TABLE `MenuOption` (
	`menuOptionId`	int(20)	NOT NULL,
	`menuId`	int(20)	NOT NULL,
	`option`	varchar(255)	NOT NULL,
	`content`	varchar(255)	NOT NULL,
	`price`	int(11)	NOT NULL,
	`createdDate`	timestamp	NOT NULL,
	`modifiedDate`	timestamp	NULL,
	`status`	varchar(255)	NOT NULL	DEFAULT '일반'
);

CREATE TABLE `Address` (
	`addressId`	varchar(50)	NOT NULL,
	`userId`	int(20)	NOT NULL,
	`address`	varchar(50)	NOT NULL,
	`detailAddress`	varchar(50)	NULL,
	`alias`	varchar(50)	NULL,
	`latitude`	decimal(13,10)	NOT NULL,
	`longitude`	decimal(13,10)	NOT NULL,
	`createdDate`	timestamp	NOT NULL,
	`modifedDate`	timestamp	NULL,
	`status`	varchar(255)	NOT NULL	DEFAULT '일반'
);

ALTER TABLE `User` ADD CONSTRAINT `PK_USER` PRIMARY KEY (
	`userId`
);

ALTER TABLE `Coupons` ADD CONSTRAINT `PK_COUPONS` PRIMARY KEY (
	`couponId`
);

ALTER TABLE `Orders` ADD CONSTRAINT `PK_ORDERS` PRIMARY KEY (
	`orderId`
);

ALTER TABLE `Reviews` ADD CONSTRAINT `PK_REVIEWS` PRIMARY KEY (
	`reviewId`
);

ALTER TABLE `Stores` ADD CONSTRAINT `PK_STORES` PRIMARY KEY (
	`storeId`
);

ALTER TABLE `Menu` ADD CONSTRAINT `PK_MENU` PRIMARY KEY (
	`menuId`
);

